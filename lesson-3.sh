# commands covered in this lesson:
# 1. pwd - stands for 'print working directory', show your current location on the system in the shell
# 2. ls - stands for 'list', lists files to standard output
# 3. cd - change directory
# 4. open - opens file according to system defaults (for this shell file, it turns out it was XCode on mac)
# 5. clear - clear the current visible part of the window in terminal (but doesn't actually delete any output history)
# 6. cat - stands for 'conCATenate', prints the contents of a file to standard output