import sys

if len(sys.argv) < 2:
    print("Not enough arguments for this script!")

if len(sys.argv) == 2:
    print("Argument 1 is: " + sys.argv[1])

if len(sys.argv) == 3:
    print("Argument 1 is: " + sys.argv[1])
    print("Argument 2 is: " + sys.argv[2])

if len(sys.argv) == 4:
    print("Argument 1 is: " + sys.argv[1])
    print("Argument 2 is: " + sys.argv[2])
    print("Argument 3 is: " + sys.argv[3])