# Commands and syntax covered in this lesson
# 1. > - output redirection - redirect the output of what is on the left to the right
# 2. < - input redirection - read or take input of what is on the right to the left
# 3. | - piping - pass on the output to the next command
# 4. tr - translates specific characters
# 5. tr [a-z] [A-Z] - translates all lowercase letters a through z to all uppercase letters A-Z

